#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char *begin;

	begin = dest;
	dest = dest + ft_strlen(dest);
	while (*src != '\0')
		*dest++ = *src++;
	*dest = '\0';
	return (begin);
}

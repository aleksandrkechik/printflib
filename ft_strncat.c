#include "libft.h"

char	*ft_strncat(char *dest, const char *src, int nb)
{
	char	*begin;
	int		index;

	begin = dest;
	dest = dest + ft_strlen(dest);
	index = 0;
	while (*src != '\0' && index < nb)
	{
		*dest++ = *src++;
		index++;
	}
	*dest = '\0';
	return (begin);
}

#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*new_str;
	size_t	i;

	new_str = (char*)malloc((size + 1) * sizeof(char));
	if (!new_str)
		return (NULL);
	i = 0;
	while (i <= size)
	{
		new_str[i] = 0;
		i++;
	}
	return (new_str);
}
